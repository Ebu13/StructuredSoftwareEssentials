package org.example;

public class PublishPlace {
    private String publishPlaceName;
    private String address;
    private String phoneNumber;

    public String getPublishPlaceName() {
        return publishPlaceName;
    }

    public void setPublishPlaceName(String publishPlaceName) {
        this.publishPlaceName = publishPlaceName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
